def diagonalDifference(arr):
    # Write your code here
    n = len(arr)
    l, r = 0, 0
    for i in range(n):
        l += arr[i][i]
        r += arr[i][n-i-1]
    d = l - r
    if d<0 :
        d *= -1
    return d

